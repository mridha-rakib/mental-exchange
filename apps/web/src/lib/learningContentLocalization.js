const getLanguage = (language) => (language === 'EN' ? 'EN' : 'DE');

const normalize = (value) => String(value || '').trim();

const getLocalizedString = (currentValue, localizedValues, language) => {
  if (!localizedValues) return currentValue;

  const target = localizedValues[getLanguage(language)] || localizedValues.EN || localizedValues.DE || currentValue;
  const current = normalize(currentValue);
  const knownValues = Object.values(localizedValues).map(normalize).filter(Boolean);

  return !current || knownValues.includes(current) ? target : currentValue;
};

const localizeStringArray = (items, localizedItems, language) => {
  if (!Array.isArray(items) || !Array.isArray(localizedItems)) return items;

  return items.map((item) => {
    const match = localizedItems.find((localizedItem) =>
      Object.values(localizedItem).map(normalize).includes(normalize(item)),
    );

    return match ? getLocalizedString(item, match, language) : item;
  });
};

const localizeFaq = (items, localizedItems, language) => {
  if (!Array.isArray(items) || !Array.isArray(localizedItems)) return items;

  return items.map((item) => {
    const match = localizedItems.find((localizedItem) =>
      Object.values(localizedItem.question).map(normalize).includes(normalize(item?.question)),
    );

    if (!match) return item;

    return {
      ...item,
      question: getLocalizedString(item.question, match.question, language),
      answer: getLocalizedString(item.answer, match.answer, language),
    };
  });
};

const localizeAttachments = (attachments, localizedAttachments, language) => {
  if (!Array.isArray(attachments) || !Array.isArray(localizedAttachments)) return attachments;

  return attachments.map((attachment, index) => {
    const localizedAttachment = localizedAttachments[index];
    if (!localizedAttachment?.label) return attachment;

    return {
      ...attachment,
      label: getLocalizedString(attachment.label, localizedAttachment.label, language),
    };
  });
};

export const localizeLearningPackage = (packageData, language) => {
  if (!packageData) return packageData;

  const copy = PACKAGE_COPY[packageData.slug];
  if (!copy) return packageData;

  return {
    ...packageData,
    title: getLocalizedString(packageData.title, copy.title, language),
    subtitle: getLocalizedString(packageData.subtitle, copy.subtitle, language),
    shortDescription: getLocalizedString(packageData.shortDescription, copy.subtitle, language),
    description: getLocalizedString(packageData.description, copy.description, language),
    longDescription: getLocalizedString(packageData.longDescription, copy.description, language),
    heroCopy: getLocalizedString(packageData.heroCopy, copy.heroCopy, language),
    targetAudience: getLocalizedString(packageData.targetAudience, copy.targetAudience, language),
    valuePoints: localizeStringArray(packageData.valuePoints, copy.valuePoints, language),
    includedContent: localizeStringArray(packageData.includedContent, copy.includedContent, language),
    faq: localizeFaq(packageData.faq, copy.faq, language),
    modules: Array.isArray(packageData.modules)
      ? packageData.modules.map((moduleRecord) => localizeLearningModule(moduleRecord, language))
      : packageData.modules,
  };
};

export const localizeLearningPackageList = (packages, language) =>
  Array.isArray(packages) ? packages.map((packageData) => localizeLearningPackage(packageData, language)) : packages;

export const localizeLearningLesson = (lesson, language) => {
  if (!lesson) return lesson;

  const copy = LESSON_COPY[lesson.slug];
  if (!copy) return lesson;

  return {
    ...lesson,
    title: getLocalizedString(lesson.title, copy.title, language),
    description: getLocalizedString(lesson.description, copy.description, language),
    textContent: getLocalizedString(lesson.textContent, copy.textContent, language),
    attachments: localizeAttachments(lesson.attachments, copy.attachments, language),
  };
};

export const localizeLearningModule = (moduleRecord, language) => {
  if (!moduleRecord) return moduleRecord;

  const copy = MODULE_COPY[moduleRecord.slug];
  const localizedModule = copy
    ? {
      ...moduleRecord,
      title: getLocalizedString(moduleRecord.title, copy.title, language),
      description: getLocalizedString(moduleRecord.description, copy.description, language),
      shortText: getLocalizedString(moduleRecord.shortText, copy.description, language),
    }
    : moduleRecord;

  return {
    ...localizedModule,
    lessons: Array.isArray(localizedModule.lessons)
      ? localizedModule.lessons.map((lesson) => localizeLearningLesson(lesson, language))
      : localizedModule.lessons,
  };
};

export const localizeLearningDashboard = (dashboard, language) => {
  if (!dashboard) return dashboard;

  return {
    ...dashboard,
    package: localizeLearningPackage(dashboard.package, language),
    availablePackages: localizeLearningPackageList(dashboard.availablePackages, language),
    modules: Array.isArray(dashboard.modules)
      ? dashboard.modules.map((moduleRecord) => localizeLearningModule(moduleRecord, language))
      : dashboard.modules,
    recentlyOpened: Array.isArray(dashboard.recentlyOpened)
      ? dashboard.recentlyOpened.map((lesson) => localizeLearningLesson(lesson, language))
      : dashboard.recentlyOpened,
  };
};

